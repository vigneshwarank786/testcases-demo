function week(day) {

    if (day == 1) {
        return "monday"
    }
    else if(day==2){
        return "tuesday"
    }
    else if(day==3){
        return "wednesday"
    }
    else if(day==4){
        return "thursday"
    }
    else if(day==5){
        return "friday"
    }
    else if(day==6){
        return "saturday"
    }
    else if(day==7){
        return "monday"
    }
}

module.exports=week;